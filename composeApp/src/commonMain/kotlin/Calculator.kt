class Calculator {

    // A function which add 2 values
    fun add(left: Int, right: Int): Int{
        return left + right
    }

    //A function which subtract 2 values
    fun subtract(left: Int, right: Int): Int{
        return left - right
    }

    //A function which multiply 2 values
    fun multiply(left: Int, right: Int): Int{
        return left * right
    }

    //A function which divide 2 values
    fun divide(left: Int, right: Int): Int {
        return left / right
    }

}